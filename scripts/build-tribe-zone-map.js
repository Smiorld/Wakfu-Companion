const fs = require("fs");
const path = require("path");

const SOURCE_CSV = path.join(
  __dirname,
  "..",
  "artifacts",
  "tribe_zone_zh_patch_table.csv"
);
const MANUAL_SOURCE_CSV = path.join(
  __dirname,
  "..",
  "artifacts",
  "tribe_manual_fill_template.csv"
);
const AREA_CHALLENGE_MAP = path.join(
  __dirname,
  "..",
  "public",
  "assets",
  "js",
  "data",
  "area_challenge_i18n.js"
);
const OUTPUT_JS = path.join(
  __dirname,
  "..",
  "public",
  "assets",
  "js",
  "data",
  "tribe_challenge_zones.js"
);

function parseCsvLine(line) {
  const values = [];
  let current = "";
  let inQuotes = false;

  for (let index = 0; index < line.length; index += 1) {
    const char = line[index];

    if (char === '"') {
      if (inQuotes && line[index + 1] === '"') {
        current += '"';
        index += 1;
      } else {
        inQuotes = !inQuotes;
      }
      continue;
    }

    if (char === "," && !inQuotes) {
      values.push(current);
      current = "";
      continue;
    }

    current += char;
  }

  values.push(current);
  return values;
}

function loadCsvRows(filePath) {
  const csv = fs.readFileSync(filePath, "utf8").replace(/^\uFEFF/, "");
  const lines = csv.split(/\r?\n/).filter(Boolean);
  const header = parseCsvLine(lines[0]);

  return lines.slice(1).map((line) => {
    const columns = parseCsvLine(line);
    const row = {};
    for (let index = 0; index < header.length; index += 1) {
      row[header[index]] = columns[index] || "";
    }
    return row;
  });
}

function loadKnownTribeIds(filePath) {
  const text = fs.readFileSync(filePath, "utf8");
  return new Set(
    text
      .split(/\r?\n/)
      .map((line) => line.trim())
      .filter((line) => line.startsWith('"-') && line.includes('":'))
      .map((line) => line.split('"', 3)[1])
  );
}

function normalizeLocationDisplay(islandZh, zoneZhFinal) {
  const island = String(islandZh || "").trim();
  const zone = String(zoneZhFinal || "").trim();
  if (!zone) return island;
  if (!island) return zone;

  const compactIsland = island.replace(/\s+/g, "");
  const compactZone = zone.replace(/\s+/g, "");
  if (compactZone.startsWith(compactIsland)) return zone;

  return `${island} ${zone}`;
}

function buildZoneMap(rows, knownTribeIds) {
  const mapping = new Map();

  for (const row of rows) {
    const challengeIds = String(row.sampleQuestIds || "")
      .split("|")
      .map((part) => part.trim())
      .filter(Boolean)
      .filter((id) => knownTribeIds.has(id));

    if (!challengeIds.length) continue;

    const entry = {
      zoneId: String(row.zoneId || "").trim(),
      islandZh: String(row.islandZh || "").trim(),
      zoneZh: String(row.zoneZhFinal || "").trim(),
      display: normalizeLocationDisplay(row.islandZh, row.zoneZhFinal),
    };

    if (!entry.display) continue;

    for (const challengeId of challengeIds) {
      if (!mapping.has(challengeId)) {
        mapping.set(challengeId, entry);
      }
    }
  }

  return [...mapping.entries()].sort((left, right) => Number(left[0]) - Number(right[0]));
}

function buildManualZoneMap(rows, knownTribeIds) {
  const mapping = new Map();

  for (const row of rows) {
    const challengeId = String(row.challengeId || "").trim();
    if (!challengeId) continue;

    const islandZh = String(row.islandZhManual || "").trim();
    const zoneZh = String(row.zoneZhFinalManual || "").trim();
    const display = normalizeLocationDisplay(islandZh, zoneZh);
    if (!display) continue;

    mapping.set(challengeId, {
      zoneId: "",
      islandZh,
      zoneZh,
      display,
    });
  }

  return mapping;
}

function buildOutput(entries) {
  const body = entries
    .map(
      ([challengeId, entry]) => `  "${challengeId}": ${JSON.stringify(entry)}`
    )
    .join(",\n");

  return `// Auto-generated by scripts/build-tribe-zone-map.js
// Source: artifacts/tribe_zone_zh_patch_table.csv + artifacts/tribe_manual_fill_template.csv
// Scope: tribe challenge ids with known Chinese location labels

const TRIBE_CHALLENGE_ZONE_MAP = Object.freeze({
${body}
});

function getTribeChallengeZoneInfo(challengeId) {
  return TRIBE_CHALLENGE_ZONE_MAP[String(challengeId)] || null;
}

function getTribeChallengeLocation(challengeId) {
  return getTribeChallengeZoneInfo(challengeId)?.display || "";
}
`;
}

function main() {
  const rows = loadCsvRows(SOURCE_CSV);
  const manualRows = fs.existsSync(MANUAL_SOURCE_CSV) ? loadCsvRows(MANUAL_SOURCE_CSV) : [];
  const knownTribeIds = loadKnownTribeIds(AREA_CHALLENGE_MAP);
  const autoEntries = buildZoneMap(rows, knownTribeIds);
  const manualEntries = buildManualZoneMap(manualRows, knownTribeIds);
  const merged = new Map(autoEntries);

  for (const [challengeId, entry] of manualEntries.entries()) {
    merged.set(challengeId, entry);
  }

  const entries = [...merged.entries()].sort((left, right) => Number(left[0]) - Number(right[0]));
  fs.writeFileSync(OUTPUT_JS, buildOutput(entries), "utf8");
  console.log(
    `Built ${entries.length} tribe zone mappings (${manualEntries.size} manual overrides) -> ${OUTPUT_JS}`
  );
}

main();
