const fs = require("fs");
const path = require("path");
const vm = require("vm");

const DATABASE_JS = path.join(__dirname, "..", "public", "assets", "js", "data", "database.js");
const I18N_JSON = path.join(__dirname, "..", "artifacts", "wakfu-i18n", "wakfu_i18n_en_zh.json");
const OUTPUT_JS = path.join(__dirname, "..", "public", "assets", "js", "data", "class_spells_zh.js");
const REPORT_JSON = path.join(__dirname, "..", "artifacts", "class-spells-zh-report.json");
const MANUAL_ZH_JSON = path.join(__dirname, "..", ".local", "class_spells_zh_manual_input.json");

function loadClassSpells() {
  const source = fs.readFileSync(DATABASE_JS, "utf8");
  const wrapped = `${source}\nmodule.exports = { classSpells };`;
  const sandbox = {
    module: { exports: {} },
    exports: {},
  };
  vm.runInNewContext(wrapped, sandbox, { filename: DATABASE_JS });
  const classSpells = sandbox.module.exports?.classSpells;
  if (!classSpells || typeof classSpells !== "object") {
    throw new Error("Failed to load classSpells from database.js");
  }
  return classSpells;
}

function loadI18nEntries() {
  const parsed = JSON.parse(fs.readFileSync(I18N_JSON, "utf8"));
  if (!Array.isArray(parsed)) {
    throw new Error("wakfu_i18n_en_zh.json is not an array.");
  }
  return parsed;
}

function loadManualZhEntries() {
  if (!fs.existsSync(MANUAL_ZH_JSON)) {
    return {};
  }

  const parsed = JSON.parse(fs.readFileSync(MANUAL_ZH_JSON, "utf8"));
  if (!parsed || typeof parsed !== "object" || Array.isArray(parsed)) {
    throw new Error("class_spells_zh_manual_input.json must be an object.");
  }

  return parsed;
}

function normalizeText(value) {
  return String(value || "").trim();
}

function scoreCandidate(entry) {
  const key = String(entry?.key || "");
  let score = 0;

  if (key.startsWith("content.3.")) score += 100;
  else if (key.startsWith("content.12.")) score += 70;
  else if (key.startsWith("content.13.")) score += 40;

  if (normalizeText(entry?.english) === normalizeText(entry?.chinese)) score -= 30;
  if (/^\[se\]/i.test(String(entry?.english || ""))) score -= 50;
  if (/^\[se\]/i.test(String(entry?.chinese || ""))) score -= 50;

  const chinese = normalizeText(entry?.chinese);
  if (chinese) {
    score += 10;
    score -= chinese.length * 0.01;
  }

  return score;
}

function chooseBestChinese(candidates) {
  if (!candidates.length) return "";
  return [...candidates]
    .sort((left, right) => {
      const scoreDiff = scoreCandidate(right) - scoreCandidate(left);
      if (scoreDiff !== 0) return scoreDiff;

      const leftLength = normalizeText(left?.chinese).length;
      const rightLength = normalizeText(right?.chinese).length;
      if (leftLength !== rightLength) return leftLength - rightLength;

      return String(left?.key || "").localeCompare(String(right?.key || ""));
    })[0]?.chinese || "";
}

function buildEnglishLookup(entries) {
  const lookup = new Map();

  entries.forEach((entry) => {
    const english = normalizeText(entry?.english);
    const chinese = normalizeText(entry?.chinese);
    if (!english || !chinese) return;

    if (!lookup.has(english)) {
      lookup.set(english, []);
    }
    lookup.get(english).push({
      key: String(entry?.key || ""),
      english,
      chinese,
    });
  });

  return lookup;
}

function buildZhSpellMap(classSpells, englishLookup, manualZhEntries) {
  const output = {};
  const unresolved = [];
  const manualMergeSummary = [];
  let resolvedCount = 0;
  let totalCount = 0;

  Object.entries(classSpells).forEach(([className, langData]) => {
    const englishSpells = Array.isArray(langData?.en) ? langData.en : [];
    const zhSpells = [];
    const missing = [];

    englishSpells.forEach((englishSpell) => {
      totalCount += 1;
      const candidates = englishLookup.get(normalizeText(englishSpell)) || [];
      const chinese = chooseBestChinese(candidates);
      if (chinese) {
        zhSpells.push(chinese);
        resolvedCount += 1;
      } else {
        missing.push(englishSpell);
      }
    });

    const manualZhSpells = Array.isArray(manualZhEntries?.[className])
      ? manualZhEntries[className].map((value) => normalizeText(value)).filter(Boolean)
      : [];
    const finalZhSpells = [...new Set([...zhSpells, ...manualZhSpells])];

    output[className] = {
      zh: finalZhSpells,
    };

    if (manualZhSpells.length) {
      const autoZhSet = new Set(zhSpells);
      manualMergeSummary.push({
        className,
        manualCount: manualZhSpells.length,
        addedCount: manualZhSpells.filter((spell) => !autoZhSet.has(spell)).length,
      });
    }

    if (missing.length) {
      unresolved.push({
        className,
        missing,
      });
    }
  });

  return {
    output,
    unresolved,
    manualMergeSummary,
    resolvedCount,
    totalCount,
  };
}

function buildOutputJs(classSpellsZh) {
  return `// Auto-generated by scripts/build-class-spell-zh-map.js
// Source: public/assets/js/data/database.js + artifacts/wakfu-i18n/wakfu_i18n_en_zh.json
// Scope: Chinese spell names mapped by class for combat player detection

const classSpellsZh = Object.freeze(${JSON.stringify(classSpellsZh, null, 2)});
`;
}

function main() {
  const classSpells = loadClassSpells();
  const entries = loadI18nEntries();
  const manualZhEntries = loadManualZhEntries();
  const englishLookup = buildEnglishLookup(entries);
  const result = buildZhSpellMap(classSpells, englishLookup, manualZhEntries);

  fs.writeFileSync(OUTPUT_JS, buildOutputJs(result.output), "utf8");
  fs.writeFileSync(
    REPORT_JSON,
    JSON.stringify(
      {
        totalCount: result.totalCount,
        resolvedCount: result.resolvedCount,
        unresolvedCount: result.totalCount - result.resolvedCount,
        unresolved: result.unresolved,
        manualMergeSummary: result.manualMergeSummary,
      },
      null,
      2
    ),
    "utf8"
  );

  console.log(
    JSON.stringify(
      {
        output: OUTPUT_JS,
        report: REPORT_JSON,
        totalCount: result.totalCount,
        resolvedCount: result.resolvedCount,
        unresolvedCount: result.totalCount - result.resolvedCount,
        manualMergeSummary: result.manualMergeSummary,
      },
      null,
      2
    )
  );
}

main();
