const fs = require("fs");
const path = require("path");

const SOURCE_CSV = path.join(
  __dirname,
  "..",
  "artifacts",
  "wakfu-i18n",
  "wakfu_i18n_en_zh.csv"
);
const MANUAL_SOURCE_CSV = path.join(
  __dirname,
  "..",
  "artifacts",
  "tribe_manual_fill_template.csv"
);
const OUTPUT_JS = path.join(
  __dirname,
  "..",
  "public",
  "assets",
  "js",
  "data",
  "area_challenge_i18n.js"
);

function parseCsvLine(line) {
  const values = [];
  let current = "";
  let inQuotes = false;

  for (let index = 0; index < line.length; index += 1) {
    const char = line[index];

    if (char === '"') {
      if (inQuotes && line[index + 1] === '"') {
        current += '"';
        index += 1;
      } else {
        inQuotes = !inQuotes;
      }
      continue;
    }

    if (char === "," && !inQuotes) {
      values.push(current);
      current = "";
      continue;
    }

    current += char;
  }

  values.push(current);
  return values;
}

function loadTribeChallengeMap() {
  const csv = fs.readFileSync(SOURCE_CSV, "utf8").replace(/^\uFEFF/, "");
  const lines = csv.split(/\r?\n/).filter(Boolean);
  const titleRows = [];
  const descriptionById = new Map();

  for (let index = 1; index < lines.length; index += 1) {
    const columns = parseCsvLine(lines[index]);
    if (columns.length < 3) continue;

    const [key, english, chinese] = columns;
    if (/^content\.24\.-\d+$/.test(key)) {
      const id = key.replace("content.24.", "");
      descriptionById.set(id, { english, chinese });
      continue;
    }

    if (!/^content\.26\.-\d+$/.test(key)) continue;
    if (!/^Collaborative\s*:/.test(english)) continue;

    const id = key.replace("content.26.", "");
    titleRows.push({ id, key, english, chinese });
  }

  const rows = titleRows.filter(({ id }) => {
    const description = descriptionById.get(id)?.english || "";
    return description.startsWith("Hordes are proliferating in the area.");
  });

  rows.sort((left, right) => Number(left.id) - Number(right.id));
  return rows;
}

function loadManualTribeChallengeMap() {
  if (!fs.existsSync(MANUAL_SOURCE_CSV)) return [];

  const csv = fs.readFileSync(MANUAL_SOURCE_CSV, "utf8").replace(/^\uFEFF/, "");
  const lines = csv.split(/\r?\n/).filter(Boolean);
  if (lines.length <= 1) return [];

  const header = parseCsvLine(lines[0]);
  return lines
    .slice(1)
    .map((line) => {
      const columns = parseCsvLine(line);
      const row = {};
      for (let index = 0; index < header.length; index += 1) {
        row[header[index]] = columns[index] || "";
      }
      return row;
    })
    .map((row) => ({
      id: String(row.challengeId || "").trim(),
      chinese: String(row.currentZhName || "").trim(),
    }))
    .filter((row) => row.id && row.chinese);
}

function buildOutput(rows) {
  const mapEntries = rows
    .map(({ id, chinese }) => `  "${id}": "${chinese}"`)
    .join(",\n");

  return `// Auto-generated by scripts/build-area-challenge-map.js
// Source: artifacts/wakfu-i18n/wakfu_i18n_en_zh.csv
// Scope: collaborative area challenges whose description matches the tribe/horde event template

const AREA_CHALLENGE_ZH_MAP = Object.freeze({
${mapEntries}
});

function getAreaChallengeChineseName(challengeId) {
  return AREA_CHALLENGE_ZH_MAP[String(challengeId)] || "";
}
`;
}

function main() {
  const rows = loadTribeChallengeMap();
  const manualRows = loadManualTribeChallengeMap();
  const merged = new Map(rows.map((row) => [row.id, row]));

  for (const row of manualRows) {
    if (!merged.has(row.id)) {
      merged.set(row.id, row);
    }
  }

  const finalRows = [...merged.values()].sort((left, right) => Number(left.id) - Number(right.id));
  const output = buildOutput(finalRows);
  fs.writeFileSync(OUTPUT_JS, output, "utf8");
  console.log(`Built ${finalRows.length} area challenge mappings -> ${OUTPUT_JS}`);
}

main();
